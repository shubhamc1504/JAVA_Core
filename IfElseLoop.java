public class IfElseLoop {
    public static void main(String args[])
    {
        int x = 18;
        int y = 20;
        
        if(x > y)
        {
            System.out.println(x);
            System.out.println("Hello");
        }
        else{
            System.out.println(y);
            System.out.println("Bye");
        }
    }
    
}
